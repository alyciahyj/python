"""
輸入三角形三邊(存入變數a, b, c中) 
判斷是否能構成三角形(利用邊長運算進行判斷，可以上網搜尋公式)
是三角形:則顯示面積和周長
不是:則顯示，無法構成三角形
​
三角形面積公式:
p = 1/2 (a+b+c)
area = (p * (p - a) * (p - b) * (p - c)) ** 0.5
​
EX:
a = 3 
b = 4
c = 5
周長:12.0
面積:6.0 
​
a = 1
b = 10
c = 100
無法構成三角形
"""
W = float(input("請輸入體重(公斤)"))
H = float(input("請輸入身高(公分)"))
bmi = W / (H / 100)**2
if bmi >= 20.7:
    print('過重')
elif bmi >= 14.8:
    print('正常')
else:
    print('過輕')
print("你的BMI指數是" + str(bmi))